def add(a, b):
    """
    Adds two numbers and returns the result.

    Args:
        a (int or float): The first number.
        b (int or float): The second number.

    Returns:
        int or float: The sum of the two numbers.
    """
    return a + b

# Example usage:
result = add(5, 7)
print("The sum is:", result)  # Output: The sum is: 12
